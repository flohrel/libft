#include "tester.h"

void	bzero_test(FILE *file, int origin, int id)
{
	int		i;
	char	str[10][10] = {
		"0000000000",
		"1111111111",
		"2222222222",
		"3333333333",
		"4444444444",
		"5555555555",
		"6666666666",
		"7777777777",
		"8888888888",
		"9999999999"
	};
	void	(*f[2])(void *, size_t) = { ft_bzero, bzero };

	(void)id;
	i = -1;
	while (++i < 10)
	{
		CHRONO(f[origin](&str[i][10 - i], i));
		fwrite(str[i], sizeof(char), 10, file);
		fprintf(file, "\n");
	}
}
